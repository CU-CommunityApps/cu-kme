package org.kuali.mobility.itnotices.entity;

import java.io.Serializable;

public class ITNotice implements Serializable {

	private static final long serialVersionUID = -3084595321218098611L;
	
	private String lastUpdated;
	private String noticeType;
	private String title;
	private String service;
	private String message;
	private String imageUrl;
	
	public ITNotice(String lastUpdated, String noticeType, String title, String service, String message) {
		this.lastUpdated = lastUpdated;
		this.noticeType = noticeType;
		this.title = title;
		this.service = service;
		this.message = message;
	}

	public String getLastUpdated() {
		return lastUpdated;
	}

	public void setLastUpdated(String lastUpdated) {
		this.lastUpdated = lastUpdated;
	}

	public String getNoticeType() {
		return noticeType;
	}

	public void setNoticeType(String noticeType) {
		this.noticeType = noticeType;
	}

	public String getService() {
		return service;
	}

	public void setService(String service) {
		this.service = service;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getImageUrl() {
		return imageUrl;
	}

	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	
}
